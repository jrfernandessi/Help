    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = null;
        int codigo = Integer.parseInt(campoBusca.getText());
        FilmeControle controle = new FilmeControle();
        try {
            Filme filme = controle.buscarPorCodigo(codigo);
            modelo = (DefaultTableModel) tabelaResultado.getModel();
            while (modelo.getRowCount() > 0) {
                modelo.removeRow(0);
            }

            modelo.addRow(new Object[]{
                filme.getCodigo(), filme.getNome()
            });

        } catch (BuscaException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

