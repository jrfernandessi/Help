    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = null;
        String nome = campoBusca.getText();
        FilmeControle controle = new FilmeControle();
        try {
            ArrayList<Filme> filmes = controle.buscarPorNome(nome);
            modelo = (DefaultTableModel) tabelaResultado.getModel();
            while (modelo.getRowCount() > 0) {
                modelo.removeRow(0);
            }
            for (Filme filme : filmes) {
                modelo.addRow(new Object[]{
                    filme.getCodigo(), filme.getNome()
                });
            }
        } catch (BuscaException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

